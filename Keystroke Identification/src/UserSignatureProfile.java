public class UserSignatureProfile {

	
	private StringProfile userNameProfile;
	private StringProfile passwordProfile;
	private StringProfile phraseProfile;
	
	public StringProfile getUserNameProfile() {
		return userNameProfile;
	}
	
	public void setUserNameProfile(StringProfile userNameProfile) {
		this.userNameProfile = userNameProfile;
	}
	
	public StringProfile getPasswordProfile() {
		return passwordProfile;
	}
	
	public void setPasswordProfile(StringProfile passwordProfile) {
		this.passwordProfile = passwordProfile;
	}
	
	public StringProfile getPhraseProfile() {
		return phraseProfile;
	}
	
	public void setPhraseProfile(StringProfile phraseProfile) {
		this.phraseProfile = phraseProfile;
	}
	
}
